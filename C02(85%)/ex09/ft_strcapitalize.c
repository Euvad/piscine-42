char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int	i;
	int	_i;

	i = 0;
	_i = 1;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			if (_i == 1)
				str[i] -= 32;
			_i = 0;
		}
		else if (str[i] >= '0' && str[i] <= '9')
			_i = 0;
		else
			_i = 1;
		i++;
	}
	return (str);
}
